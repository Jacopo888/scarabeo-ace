export const isValidWord = (word: string): boolean => {
  return ENABLE_WORDS.has(word.toUpperCase())
}

export const validateWords = (words: string[]): { valid: string[], invalid: string[] } => {
  const valid: string[] = []
  const invalid: string[] = []
  
  words.forEach(word => {
    if (isValidWord(word)) {
      valid.push(word)
    } else {
      invalid.push(word)
    }
  })
  
  return { valid, invalid }
}